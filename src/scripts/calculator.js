export default class Calculator {
  static add(argNum1, argNum2) {
    return argNum1 + argNum2;
  }
  static subtract(argNum1, argNum2) {
    return argNum1 - argNum2;
  }
  static divide(argNum1, argNum2) {
    return argNum1 / argNum2;
  }
  static multiply(argNum1, argNum2) {
    return argNum1 * argNum2;
  }
}
